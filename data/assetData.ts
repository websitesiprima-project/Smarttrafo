export const getGIHealthStatus = (giName: string) => {
  const trafos = (trafoDatabase as Record<string, any[]>)[giName] || [];
  if (trafos.some((t) => t.status === "Kritis")) return "Critical";
  if (trafos.some((t) => t.status === "Waspada")) return "Warning";
  return "Normal";
};
